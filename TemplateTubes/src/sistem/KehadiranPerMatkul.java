public class KehadiranPerMatkul {
    private Matkul matkul;
    private float persenKehadiran;
    private int totKehadiran;

    public Matkul getMatkul() {
        return matkul;
    }

    public void setMatkul(Matkul matkul) {
        this.matkul = matkul;
    }

    public float getPersenKehadiran() {
        return persenKehadiran;
    }

    public void setPersenKehadiran(float persenKehadiran) {
        this.persenKehadiran = persenKehadiran;
    }

    public int getTotKehadiran() {
        return totKehadiran;
    }

    public void setTotKehadiran(int totKehadiran) {
        this.totKehadiran = totKehadiran;
    }
    
    
}
